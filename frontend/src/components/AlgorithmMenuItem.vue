<template>
  <div class="card">
    <div class="card-body">
      <h5 class="card-title">{{ algo.title }}</h5>
      <form @submit="applyAlgorithmSubmit($event)">
        <input type="hidden" name="algorithm" :value="algo.name" required />
        <div class="form-group row" v-for="arg in algo.args" :key="arg.name">
          <label class="col-sm-6 col-form-label">{{ arg.title }}</label>
          <div class="col-sm-6">
            <select
              v-if="arg.type == 'select'"
              class="form-control"
              :name="arg.name"
              :required="arg.required"
            >
              <option
                v-for="option in arg.options"
                :key="option.name"
                :value="option.name"
                :title="option.title"
              >
                {{ option.title }}
              </option>
            </select>
            <input
              v-else
              :type="arg.type"
              class="form-control"
              :placeholder="arg.title"
              :title="arg.title"
              :name="arg.name"
              :min="arg.min"
              :max="arg.max"
              :required="arg.required"
            />
          </div>
        </div>
        <div
          class="alert alert-danger alert-dismissible fade show"
          v-for="err in errors"
          :key="err.type"
          role="alert"
        >
          <strong>Error :</strong> {{ err.message }}
        </div>
        <div class="form-row">
          <span class="col-sm-6"></span>
          <div class="col-sm-6">
            <button type="submit" class="btn btn-primary mx-4">Apply</button>
          </div>
        </div>
      </form>
    </div>
  </div>
</template>

<script>
import httpApi from "../http-api.js";
export default {
  name: "AlgorithmMenuItem",
  props: {
    imageId: { required: true, type: Number },
    algo: { required: true, type: Object },
    parentComponent: { required: true, type: Object },
  },
  data() {
    return {
      errors: [],
    };
  },
  methods: {
    applyAlgorithmSubmit(e) {
      e.preventDefault();

      let query = new URLSearchParams();
      for (const field of e.target.elements)
        if (field.name && (field.required || field.value))
          query.append(field.name, field.value);

      let queryString = query.toString();

      httpApi
        .get_image_with_algo(this.imageId, queryString)
        .then((res) => {
          this.errors = [];
          this.parentComponent.showProcessedImage(res.data);
        })
        .catch((err) => {
          this.errors = [];
          this.errors.push(err);
        });
    },
  },
};
</script>